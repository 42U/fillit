#include "libft.h"

void	ft_place_ju(char c, int i, int j)
{
	g_square[i][j] = c;
	g_square[i + 1][j] = c;
	g_square[i + 1][j + 1] = c;
	g_square[i + 1][j + 2] = c;
}
