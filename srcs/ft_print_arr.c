#include "libft.h"

void	ft_print_arr()
{
	int i;
	int j;

	i = 0;
	while (i < g_base)
	{
		j = 0;
		while (j < g_base)
		{
			ft_putchar(g_square[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
