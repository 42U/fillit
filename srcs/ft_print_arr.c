#include "libft.h"

void	ft_print_arr()
{
	int i;
	int j;

	i = -1;
	while (++i < g_base)
	{
		j = -1;
		while (++j < g_base)
			ft_putchar(g_square[i][j]);
		ft_putchar('\n');
	}
}
